def truncate_for_telegram(text: str, limit: int = TELEGRAM_MESSAGE_LIMIT) -> str:
    if len(text) <= limit:
        return text
    # "…" займає один символ — ріжемо на один коротше, щоб текст разом з ним уклався в limit
    return text[: limit - 1] + "…"
